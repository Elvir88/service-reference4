import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { IPromotionOnLocation } from '@/shared/model/promotion-on-location.model';

const baseApiUrl = 'api/promotion-on-locations';

export default class PromotionOnLocationService {
  public find(id: number): Promise<IPromotionOnLocation> {
    return new Promise<IPromotionOnLocation>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl + `?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IPromotionOnLocation): Promise<IPromotionOnLocation> {
    return new Promise<IPromotionOnLocation>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IPromotionOnLocation): Promise<IPromotionOnLocation> {
    return new Promise<IPromotionOnLocation>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IPromotionOnLocation): Promise<IPromotionOnLocation> {
    return new Promise<IPromotionOnLocation>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
